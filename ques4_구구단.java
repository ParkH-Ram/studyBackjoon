package Array_;

public class ques4_구구단 {
	
	public static void main(String [] args) {
		
		int i,j;
		
		
		for( i =1; i<=9; i++ ) {
			
			System.out.print(i+ "단 \n");
			
			for ( j=1; j<=9; j++) {
				
				System.out.print(i +"X" + j + " = " + i * j + "\n" );
				
				
				
			}
			
		}
	}
	
	

}
